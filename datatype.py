#float type
x=2.5
print(x)
print(type(x))

#complex type
a=1j + 2j
print(a)
print(type(a))

#list type
b=["React","Python","SQL"]
print(b)
print(type(b))

#tuple type
c=('HTML','CSS','Javascript')
print(c)
print(type(c))

#range type
d=range(1,8)
print(d)
print(type(d))

#dictionary type
e={ 
    "name":'usha',
    "course":'MERN'}
print(e)
print(type(e))